package com.example.springbootdemo.entity;

public class User {

    private String id;
    private String name;
    // Getter for id
    public String getId() {
        return id;
    }

    // Setter for id
    public void setId(String id) {
        this.id = id;
    }

    // Getter for username
    public String getName() {
        return name;
    }

    // Setter for username
    public void setName(String name) {
        this.name = name;
    }

}
